int rec(int ind, int left) {
	if (ind >= n) return 0 ;
	if (left == 0) {
		return 1;
	}
	if (dp.find({ind, left}) != dp.end())
		return dp[ {ind, left}];

	int ans = 0;

	if ( left - arr[ind] >= 0) {
		ans = mod_add(rec(ind, left - arr[ind]), ans, mod);
	}

	ans = mod_add(rec(ind + 1, left), ans, mod);

	return dp[ {ind, left}] = ans;
}

